public class ProvaMath {
    public static void main(String[] args){
        System.out.println(Math.max(5, 10));    //trova il massimo tra 2 (non più di 2!) numeri
        System.out.println(Math.min(5, 10));    //troa il minimo tra 2 (non più di 2!) numeri
        System.out.println(Math.sqrt(64));        //fa la radice quadrata di un numero
        System.out.println(Math.abs(-3.14));        //valore assoluto
        System.out.println(Math.random());          //restituisce un numero random tra 0 e 1
        System.out.println(Math.random()*101);      //qui tra 0 e 100 
    }
}